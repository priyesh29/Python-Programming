# list literal
fruits = ["apple", "mango", "orange"] 
print(fruits)

#type function is used to know which class a variable or a value belongs to 
print(type(fruits))

#[] indexing is used to find the position of the items inside a variable
print(fruits[0])
print(fruits[2])

# tuple literal
numbers = (1, 2, 3) 
print(numbers)
print(type(numbers))
print(numbers[1])

# dictionary literal
alphabets = {'a':'apple', 'b':'ball', 'c':'cat'} 
print(alphabets)
print(type(alphabets))

# set literal
vowels = {'a', 'e', 'i' , 'o', 'u'} 
print(vowels)
print(type(vowels))

strings = 'Eric'
print(strings)
print(type(strings))
print(strings[1])