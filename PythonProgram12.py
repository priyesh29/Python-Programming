#radius of circle and PI value declared as a variable

radius = 30
PI = 3.14

#find area and circumference of the circle 

area_of_circle = PI * radius ** 2
circumference_of_circle = 2 * PI * radius

print("The Area of Circle is :", area_of_circle)
print("\nThe Data Type is : ", type(area_of_circle))
print("\nThe Circumference of Circle is :", circumference_of_circle)
print("\nThe Data Type is : ", type(circumference_of_circle))
